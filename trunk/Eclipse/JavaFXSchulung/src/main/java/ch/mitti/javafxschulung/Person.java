package ch.mitti.javafxschulung;

public class Person {
	String firstname;
	String lastname;
	int age;
	String phonenumber;
	String phonetype;
	boolean allowAllowAdvertisement;
	public String getFullname() {
		return fullname;
	}
	
	public String getFirstname() {
		return firstname;
	}

	public void setFirstname(String firstname) {
		this.firstname = firstname;
	}

	public String getLastname() {
		return lastname;
	}

	public void setLastname(String lastname) {
		this.lastname = lastname;
	}

	public int getAge() {
		return age;
	}
	public void setAge(int age) {
		this.age = age;
	}
	public String getPhonenumber() {
		return phonenumber;
	}
	public void setPhonenumber(String phonenumber) {
		this.phonenumber = phonenumber;
	}
	public String getPhonetype() {
		return phonetype;
	}
	public void setPhonetype(String phonetype) {
		this.phonetype = phonetype;
	}
	public boolean isAllowAllowAdvertisement() {
		return allowAllowAdvertisement;
	}
	public void setAllowAllowAdvertisement(boolean allowAllowAdvertisement) {
		this.allowAllowAdvertisement = allowAllowAdvertisement;
	}
	
	
}
